func generateFile(tmplStr, filename string, data interface{}) error {
	tmpl, err := template.New(filename).Funcs(funcMap).Parse(tmplStr)
	if err != nil {
		return fmt.Errorf("parse template for %s: %w", filename, err)
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("execute template for %s: %w", filename, err)
	}

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return fmt.Errorf("format %s: %w\n\nRaw output:\n%s", filename, err, buf.String())
	}

	if err := os.WriteFile(filename, formatted, 0644); err != nil {
		return fmt.Errorf("write %s: %w", filename, err)
	}

	fmt.Printf("Generated %s\n", filename)
	return nil
}

func main() {
	for _, n := range []int{2, 3, 4} {
		filename := fmt.Sprintf("vec%d.go", n)
		if err := generateFile(vecTemplate, filename, vecData{N: n}); err != nil {
			fmt.Fprintf(os.Stderr, "Error: %v\n", err)
			os.Exit(1)
		}
	}

	for _, n := range []int{2, 3, 4} {
		filename := fmt.Sprintf("mat%d.go", n)
		if err := generateFile(matTemplate, filename, matData{N: n, N2: n * n}); err != nil {
			fmt.Fprintf(os.Stderr, "Error: %v\n", err)
			os.Exit(1)
		}
	}
}
