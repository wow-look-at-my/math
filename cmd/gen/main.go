// Translation creates a translation matrix.
func Translation(v Vec3) Mat4 {
	return NewMat4(
		1, 0, 0, v[0],
		0, 1, 0, v[1],
		0, 0, 1, v[2],
		0, 0, 0, 1,
	)
}

